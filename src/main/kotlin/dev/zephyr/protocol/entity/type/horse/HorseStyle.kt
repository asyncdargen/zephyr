package dev.zephyr.protocol.entity.type.horse

/**
 * Represents the style, or markings, that the horse has.
 */
enum class HorseStyle {
    /**
     * No markings
     */
    NONE,

    /**
     * White socks or stripes
     */
    WHITE,

    /**
     * Milky splotches
     */
    WHITEFIELD,

    /**
     * Round white dots
     */
    WHITE_DOTS,

    /**
     * Small black dots
     */
    BLACK_DOTS
}
